     * Update the specified resource in storage.
     *
     * @param  \App\Http\Requests\UpdateProfileRequest  $request
     * @param  \App\Models\Profile  $profile
     * @return \Illuminate\Http\Response
     */
    public function update(UpdateProfileRequest $request, Profile $profile)
    {
        if ($request->hasFile('profile_image')) {

            Storage::delete("public/" . $profile->name);


            $newName = uniqid() . "_profile_image." . $request->file('profile_image')->extension();
            $request->file('profile_image')->storeAs('public', $newName);

            $profile->name = $newName;
        }

        $profile->update();

        return redirect()->route('profile.index')->with('status', 'Image is updated Successfully');
    }

